@Injectable({ providedIn: 'root' })
export class TranslationService {
  private readonly languageSignal = signal<AppLanguage>(this.getInitialLanguage());

  currentLanguage(): AppLanguage {
    return this.languageSignal();
  }

  setLanguage(language: AppLanguage): void {
    this.languageSignal.set(language);
    localStorage.setItem(STORAGE_KEY, language);
  }

  t(key: string, params?: Record<string, string>): string {
    const dictionary = this.currentLanguage() === 'VN' ? VN_DICTIONARY : EN_DICTIONARY;
    let value = dictionary[key] ?? EN_DICTIONARY[key] ?? key;

    if (!params) {
      return value;
    }

    for (const [paramKey, paramValue] of Object.entries(params)) {
      value = value.replaceAll(`{{${paramKey}}}`, paramValue);
    }

    return value;
  }

  tCategory(category: string): string {
    const normalized = category.trim().toLowerCase();
    return this.t(`category.${normalized}`);
  }

  private getInitialLanguage(): AppLanguage {
    const savedLanguage = localStorage.getItem(STORAGE_KEY);
    return savedLanguage === 'EN' ? 'EN' : 'VN';
  }
}
